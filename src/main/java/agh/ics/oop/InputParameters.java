package agh.ics.oop;

public class InputParameters {
    int height;
    int width;
    int initialEnergy;
    int initialNumberOfAnimals;
    int initialNumberOfPlants;
    int lostEnergy;
    int plantKcal;
    int jungleRatio;

    public int getHeight(){
        return height;
    }

    public int getWidth(){
        return width;
    }

    public int getInitialEnergy(){
        return initialEnergy;
    }

    public int getInitialNumberOfAnimals(){
        return initialNumberOfAnimals;
    }

    public int getInitialNumberOfPlants(){
        return initialNumberOfPlants;
    }

    public int getLostEnergy(){
        return lostEnergy;
    }

    public int getPlantKcal(){
        return plantKcal;
    }

    public int getJungleRatio(){
        return jungleRatio;
    }
}

